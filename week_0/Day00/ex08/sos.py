from sys import *

def main():
    morse = ''
    msg = " ".join(argv[1:])
    for char in msg:
        if char.isalnum():
             morse += CODE[char.upper()]
        elif char == " ":
            morse += '/'
        else:
            print("ERROR")
            exit()
        morse += ' '
    print(morse[:-1])
		
